/*
 2. Array
 배열이라고 부르고 순서가 정해져 있다.
 각 배열 바구니에는 같은 타입이 들어가야한다.
 */

var a: [Int] = [1, 2, 3, 4, 5] // 선언할 때 타입은 []바구니 안에 타입을 명시할 수 있고 오른쪽에도 []안에 데이터들을 넣으면 된다.
var b: Array<Int> = [1, 2, 3, 5, 4] // 이 방법이 정식선언 방법이지만 위 방법을 더 자주 사용하게 된다.

print(a == b) // a, b배열 모두 1,2,3,4,5라는 같은 데이터들이 있지만 배열에는 순서가 존재하기 때문에 같지 않다.(순서까지 같아야 같은 것으로 간주한다.)

// 빈 배열 만들기
